DROP TABLE ARTISTS;

CREATE TABLE ARTISTS (
  ARTIST_COD NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY, 
  ARTIST_NAME VARCHAR2(30),
  GENRE VARCHAR2(10) CHECK (GENRE IN ('Rock', 'Pop', 'Jazz', 'Swing', 'Blues', 'Vals')),
  BIOGRAFY CLOB DEFAULT EMPTY_CLOB(),
  IMAGE ORDIMAGE NULL,
  THUMBNALL BLOB DEFAULT EMPTY_BLOB(),
  IMG64 VARCHAR2(3200)
);
  
